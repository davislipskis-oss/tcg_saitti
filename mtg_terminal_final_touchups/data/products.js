const enrichOffers = (product, layer) => {
  const existing = new Map(product.offers.map((offer) => [offer.retailer, offer]));
  const desiredRetailers = defaultRetailersByLayer[layer] || defaultRetailersByLayer['Layer 2'];

  return desiredRetailers.map((retailer) => {
    const offer = existing.get(retailer) || {
      retailer,
      price: null,
      available: false,
      href: '#',
      freshness: 'Pending direct product URL',
    };

    return {
      ...offer,
      href: offer.href && offer.href !== '#' ? offer.href : (directProductUrl(retailer, product.slug) || retailerSearchUrl(retailer, product.name)),
    };
  });
};

